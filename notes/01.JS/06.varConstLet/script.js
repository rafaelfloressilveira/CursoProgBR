// variáveis var, const e let

// var = variável global, pode ser acessado de qualquer parte do código

// const = constante, uma vez atribuído um valor, não pode ser reatribuido

const numero = 1

numero = 2 // Isto não pode ser feito, pois estou modificando a const

// let → existe somente dentro do bloco de código { }

const numero = [4] // Criando uma array

numero.push(7); //Método push adiciona um número da array

